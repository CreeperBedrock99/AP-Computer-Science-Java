/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        String names = new String("Bob and Rob");
        int number = 5;
        double money = 72.67;
        System.out.println(names + " live in Vancouver");
        System.out.println(names + " have " + number + " Home Depots");
        System.out.println(names + " have " + money + " amount of watermelons");
	}
}